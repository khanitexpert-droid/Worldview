// lowercased index over canonical names + aliases, for tolerant lookups
const INDEX: Record<string, [number, number]> = {};
for (const [name, c] of Object.entries(COUNTRY_CENTROIDS)) {
  INDEX[name.toLowerCase()] = c;
}
for (const [alias, canon] of Object.entries(ALIASES)) {
  const c = COUNTRY_CENTROIDS[canon];
  if (c) INDEX[alias.toLowerCase()] = c;
}

/** Resolve a GDELT source-country name to a [lon, lat] anchor, or undefined. */
export function centroidFor(name: string): [number, number] | undefined {
  if (!name) return undefined;
  return INDEX[name.trim().toLowerCase()];
}
